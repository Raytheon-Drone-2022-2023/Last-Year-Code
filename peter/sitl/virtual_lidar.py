def print_map():
    print("-------------------------------", end="")
    for r in range(0, len(map)):
        for c in range(0, len(map[r])):
            print(str(map[r][c]) + " ", end="")
        print("\n", end="")
    print("-------------------------------")
    time.sleep(1)
